    private var viewBinding: ActivityMainBinding? = null
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        viewBinding = ActivityMainBinding.inflate(layoutInflater)
        setContentView(viewBinding?.root)
        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main)) { v, insets ->
            val systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars())
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom)
            insets
        }
        initViews()
    }

    override fun onDestroy() {
        super.onDestroy()
        viewBinding = null

    }

    private fun initViews() {
        viewBinding?.apply {

            loadImageButton?.setOnClickListener {
                val randomNumber = (0 until listOfPics.size).random()
                val randomImage = listOfPics[randomNumber]
                Glide.with(this@FirstActivity).load(randomImage).into(imageView)
            }

            goToSecAct?.setOnClickListener {
                startActivity(Intent(this@FirstActivity, SecondActivity::class.java))
            }
        }

    }
